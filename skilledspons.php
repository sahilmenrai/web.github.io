  		<p><strong>The Skilled Nominated or Sponsored (Provisional)</strong>    Subclass 489 visa replaces both the offshore Skilled – Regional Sponsored Subclass 475 visa and the onshore Subclass 487 visa.</p>
  		<p>It is a 3-year provisional visa which requires either sponsorship by a relative living in a designated area or a State or Territory Government.</p>


 	<p style="font-family:allura; font-size: 25px;"><strong>Skill Select Only Application</strong></p>
 	<p>In order to apply for a Skilled Nominated Subclass 190 visa, you must first lodge an Expression of Interest through SkillSelect and be invited by the Department of Immigration (DIAC) to apply.</p>

<p>Once you are invited to apply for a skilled visa, you must lodge your application within 60 days, otherwise the invitation lapses. </p>
 	<p style="font-family:allura; font-size: 25px;"><strong>Visa Criteria</strong></p>
 	<p>In order to qualify for a Subclass 489 visa, you would need to:</p>
 	
 	
 	<p><ul class="list-unstyled">
    
    
        <ul>
            <li>Be sponsored by a relative living in a Designated Area or nominated by a State or Territory Government.</li>
            <li>Meet the pass mark in the skilled migration points test- currently 60.</li>
            <li>Be aged between 18 and 49 inclusive.</li>
            <li> Be aged between 18 and 49 inclusive.Pass Skills Assessment in an occupation on the relevant Skilled Occupations List.</li>
            <li>Have Competent English or higher (6 minimum in IELTS or holder of approved passports).</li>
            <li>Meet health and character requirements.</li>
            
        </ul>
    </li>
    
</ul></p>
 	
 	
 	<p style="font-family:allura; font-size: 25px;"><strong>Sponsorship by a Relative Living in a Designated Area</strong></p>
 	<p>You can apply for a Subclass 489 visa if you are sponsored by a relative living in a Designated Area at the time they sponsor you.</p>
 	<p>The relative must be an Australian citizen, permanent resident or Eligible New Zealand Citizen. The following relatives can sponsor for the Subclass 489 visa:</p>
	
	
 	
 	
 	
 	<p><ul class="list-unstyled">
    
    
        <ul>
            <li>Parents.</li>
            <li>Brothers and Sisters.</li>
            <li>Uncles and Aunts.</li>
            <li>Nephews and Nieces.</li>
            <li>First Cousins.</li>
            <li>Grandparents</li>
            
            
        </ul>
    </li>
    
</ul></p>
	
	<p>The applicable occupations list if sponsored by a relative is the same as the list for the Skilled Independent visa.</p>
	<p>If you are sponsored by a relative, you will need to live for 2 years and work full time for 12 months in a regional or low population growth area to qualify for permanent residence through the Skilled Regional Subclass 190 visa.</p>
 	
	
 	
 	
 	
 	
 	<p style="font-family:allura; font-size: 25px;"><strong>Nomination by a State or Territory Government</strong></p>
 	<p>Each State and Territory Government publishes a list of occupations in short supply in their area. There may be specific requirements in terms of work experience, English language ability or specialised skills.</p>
 	<p>If you are nominated by a state or territory government, you will need to live for 2 years and work full time for 12 months in a regional or low population growth area to qualify for permanent residence through the Skilled Regional Subclass 887 visa.</p>
	
	<p style="font-family:allura; font-size: 25px;"><strong>On-Shore or Off-shore Lodgement</strong></p>
 	
 	
 	
 	<p><ul class="list-unstyled">
    
    
        <ul>
            <li>You can either be in Australia or overseas when you apply.</li>
            <li>To lodge from within Australia, you would need to hold a substantive visa or a Bridging A, B or C visa.</li>
            <li>Once you apply from within Australia, you would receive a bridging visa allowing you to remain in Australia during processing of your visa application.</li>
            
            
        </ul>
    </li>
    
</ul></p>
 	
	
	
	
 	
 	<p style="font-family:allura; font-size: 25px;"><strong>Including Family Members</strong></p>
 	<p>You can include the following types of family members in your application:</p>
 	
 	
 	<p><ul class="list-unstyled">
    
    
        <ol>
            <li>Spouses, defacto partners.</li>
            <li>Dependent children.</li>
            <li>Spouses, defacto partners and dependent children can be added after lodgement of the application.</li>
            <li>Other dependent family members – parents who live with you and are financially dependent. But other dependent family members must be included at lodgement.</li>
            
            
        </ol>
    
    
</ul></p>
 	
 	
 	<p style="font-family:allura; font-size: 25px;"><strong>Duration and Conditions</strong></p>
 	<p>The visa is a 3-year provisional visa. Whilst in Australia you and your included family members can only live, work and study in an approved regional area. The approved regional areas vary depending on whether you are sponsored by a relative or by a State or Territory Government:</p>
 	
 	<p><ul class="list-unstyled">
    
    
        <ul>
            <li>State or Territory Government: you can only live in a Regional or Low Population Growth Area.</li>
            <li>Relative: you can live in a Designated Area.</li>
            
            
            
        </ul>
    </li>
    
</ul></p>
	
	<p>Designated areas are wider than Regional or Low Population Growth areas – in particular the major cities of Melbourne, Canberra, and the Gold Coast are considered Designated Areas.</p>
	<p>If you are outside Australia when the visa is granted, you and your family members will need to enter Australia for the first time prior to expiry of your health and police clearances.</p>
